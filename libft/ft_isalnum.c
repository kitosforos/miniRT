#include "libft.h"

int	ft_isalnum(int c)
{
	int	i;

	i = 0;
	if (c >= '0' && c <= '9')
		i++;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		i++;
	return (i);
}
